/**
 * 根據縣市 value 獲取縣市資料
 * @param {string} cityValue - 縣市 value
 * @returns {Object|null} 縣市物件
 */
export const getCityByValue = (cityValue) => {
  return TAIWAN_CITIES.find(city => city.value === cityValue) || null;
};

/**
 * 根據縣市 value 獲取區域列表
 * @param {string} cityValue - 縣市 value
 * @returns {Array} 區域陣列
 */
export const getDistrictsByCity = (cityValue) => {
  const city = getCityByValue(cityValue);
  return city ? city.districts : [];
};

/**
 * 根據縣市和區域 value 獲取完整地址資訊
 * @param {string} cityValue - 縣市 value
 * @param {string} districtValue - 區域 value
 * @returns {Object|null} 地址資訊
 */
export const getAddressInfo = (cityValue, districtValue) => {
  const city = getCityByValue(cityValue);
  if (!city) return null;
  
  const district = city.districts.find(d => d.value === districtValue);
  if (!district) return null;
  
  return {
    city: city.name,
    cityValue: city.value,
    district: district.name,
    districtValue: district.value
  };
};